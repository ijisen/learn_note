/** ================
 *    js 错误类型
 * =================*/
// 1. ReferenceError 引用错误
throw new ReferenceError('引用错误');
// 2. TypeError 类型错误
throw new TypeError('类型错误');
// 3. SyntaxError 语法错误
throw new SyntaxError('语法错误');
// 4. URIError  给 encodeURI()或  decodeURI()传递的参数无效。
throw new URIError('url错误');
// 5. EvalError eval错误
throw new EvalError('eval error');
// 6. RangeError 范围错误
throw new RangeError('范围错误');
