package main

import "fmt"

/**
golang 标识符
--> 算术运算符:  + - * / % ++ --
--> 赋值运算符: = += -= /= *= %=
--> 比较运算符/关系运算符: > < != == <= >=
--> 逻辑运算符： ！ && ||
--> 位运算符: & | ^ << >>
--> 其它运算符:
----> & 返回变量的储存地址
----> * 指针变量

ps: go 中没有三元运算符  ? :
*/

func main() {

	a := 6
	b := 4

	fmt.Println(a, b)
	a += b    // 10
	b = a - b // 10 - 4 = 6
	a = a - b // 10 -6 = 4
	fmt.Println(a, b)
}
